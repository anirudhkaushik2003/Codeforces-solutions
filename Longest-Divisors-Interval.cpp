void solve()
{
    ll n;
    cin >> n;
    ll i;
    ll count = 0;
    for( i= 1; i<= n;i++)
    {
        if(n%i != 0)
        {
            break;
        }
        else{
            count++;
        }
    }
    cout << count << endl;
}

